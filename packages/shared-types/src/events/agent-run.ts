/**
 * The category of a single tool call, derived from tool *facts* (the tool name)
 * by the host/projection — never generated by the model and never parsed from
 * raw shell content. Renderers receive this safe category, not tool internals.
 */
export type ActivityKind =
  | 'read'
  | 'search'
  | 'write'
  | 'edit'
  | 'shell'
  | 'build'
