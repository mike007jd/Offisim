function parseDetailValue(detail?: string): unknown {
  if (!detail) return null;
  try {
    return JSON.parse(detail) as unknown;
  } catch {
    return null;
  }
}

function pickString(...values: unknown[]): string | undefined {
  for (const v of values) {
    if (typeof v === 'string' && v.length > 0) return v;
  }
  return undefined;
}

function pickRecord(value: unknown): Record<string, unknown> | null {
  return value && typeof value === 'object' && !Array.isArray(value)
    ? (value as Record<string, unknown>)
    : null;
}

function pickRecordFrom(value: unknown): Record<string, unknown> | null {
  const record = pickRecord(value);
  if (record) return record;
  if (!Array.isArray(value)) return null;
  for (const item of value) {
    const itemRecord = pickRecord(item);
    if (itemRecord) return itemRecord;
  }
  return null;
}

function pickNumber(...values: unknown[]): number | undefined {
  for (const v of values) {
    if (typeof v === 'number' && Number.isFinite(v)) return v;
  }
  return undefined;
}

function cappedString(value: unknown, max: number): string | undefined {
  if (typeof value !== 'string' || value.length === 0) return undefined;
  const normalized = value.replace(/\s+/g, ' ').trim();
  if (!normalized) return undefined;
  return normalized.length > max ? normalized.slice(0, max) : normalized;
}

const SENSITIVE_KEY_VALUE_PATTERN =
  /\b(password|passwd|pwd|passphrase|secret|token|api[_-]?key|access[_-]?key|client[_-]?secret|private[_-]?key|bearer|authorization)\b(\s*[:=]\s*|\s+)(\S+)/gi;
const SENSITIVE_TOKEN_PATTERN =
  /\b(sk-[A-Za-z0-9_-]{8,}|ghp_[A-Za-z0-9]{8,}|gho_[A-Za-z0-9]{8,}|github_pat_[A-Za-z0-9_]{8,}|xox[baprs]-[A-Za-z0-9-]{8,}|AKIA[0-9A-Z]{12,}|eyJ[A-Za-z0-9_-]{10,}\.[A-Za-z0-9_-]{5,}\.[A-Za-z0-9_-]{5,})\b/g;

/** Mask credential-shaped content in computer-use text previews; the raw typed
 * text must never reach the UI or exported traces. Mirrored in
 * `scripts/pi-mcp-bridge-extension.mjs` (emit side). */
function redactSensitiveText(text: string): string {
  return text
    .replace(SENSITIVE_KEY_VALUE_PATTERN, (_match, key: string, sep: string) => `${key}${sep}•••`)
    .replace(SENSITIVE_TOKEN_PATTERN, '•••');
}

function contentTextFrom(value: unknown): string | undefined {
  if (typeof value === 'string' && value.trim()) return value;
  if (Array.isArray(value)) {
    const text = value
      .map((part) => {
        if (typeof part === 'string') return part;
        const record = pickRecord(part);
        return record && typeof record.text === 'string' ? record.text : '';
      })
      .filter(Boolean)
      .join('\n')
      .trim();
    return text || undefined;
  }
  const record = pickRecord(value);
  if (!record) return undefined;
  return pickString(
    record.stdout,
    record.output,
    record.text,
    record.content,
    contentTextFrom(record.content),
  );
}

function summarize(value: unknown, max = 200): string | undefined {
  const text = contentTextFrom(value) ?? (value == null ? undefined : JSON.stringify(value));
  if (!text) return undefined;
  const firstLine = text.split('\n')[0] ?? text;
  return firstLine.length > max ? `${firstLine.slice(0, max)}…` : firstLine;
}

function contentBlocksFrom(value: unknown): unknown[] | null {
  if (Array.isArray(value)) return value;
  const record = pickRecord(value);
  if (!record) return null;
  if (Array.isArray(record.content)) return record.content;
  if (record.result !== undefined) return contentBlocksFrom(record.result);
  if (record.partialResult !== undefined) return contentBlocksFrom(record.partialResult);
  return null;
}

function isImageBlock(value: unknown): value is Record<string, unknown> {
  const record = pickRecord(value);
  return (
    record?.type === 'image' &&
    typeof record.mimeType === 'string' &&
    record.mimeType.startsWith('image/')
  );
}

function screenshotFromImageValue(
  value: unknown,
): { readonly mimeType: string; readonly dataRef: string } | undefined {
  const record = pickRecord(value);
  const mimeType = pickString(record?.mimeType, record?.mime_type);
  if (!mimeType?.startsWith('image/')) return undefined;
  const data = pickString(record?.data);
  const existingRef = pickString(record?.dataRef, record?.data_ref, record?.uri, record?.url);
  const dataRef = data ? `data:${mimeType};base64,${data}` : existingRef;
  return dataRef ? { mimeType, dataRef } : undefined;
}

function textBlocksFrom(blocks: readonly unknown[]): string[] {
  return blocks
    .map((block) => {
      const record = pickRecord(block);
      return record?.type === 'text' && typeof record.text === 'string' ? record.text.trim() : '';
    })
    .filter(Boolean);
}

function parseBrowserUrl(texts: readonly string[]): string | undefined {
  const joined = texts.join('\n');
  return joined.match(/https?:\/\/[^\s)"'<>]+/i)?.[0];
}

function parseBrowserTitle(texts: readonly string[], url?: string): string | undefined {
  for (const text of texts) {
    const parsed = pickRecord(parseDetailValue(text));
    const fromJson = pickString(parsed?.title, parsed?.pageTitle);
    if (fromJson) return fromJson;
    for (const line of text
      .split('\n')
      .map((l) => l.trim())
      .filter(Boolean)) {
      const labelled = line.match(/^(?:title|pageTitle)\s*[:=-]\s*(.+)$/i)?.[1]?.trim();
      if (labelled) return labelled;
    }
  }
  return texts
    .flatMap((text) =>
      text
        .split('\n')
        .map((line) => line.trim())
        .filter(Boolean),
    )
    .find((line) => line !== url && !/^https?:\/\//i.test(line) && line.length <= 160);
}

function browserDetailFrom(value: unknown): Extract<ToolRichDetail, { family: 'browser' }> | null {
  const blocks = contentBlocksFrom(value);
  if (!blocks) return null;
  const image = blocks.find(isImageBlock);
  if (!image) return null;
  const texts = textBlocksFrom(blocks);
  const url = parseBrowserUrl(texts);
  const title = parseBrowserTitle(texts, url);
  const mimeType = image.mimeType as string;
  const data = typeof image.data === 'string' ? image.data : undefined;
  const existingRef = pickString(image.dataRef, image.uri, image.url);
  const dataRef = data ? `data:${mimeType};base64,${data}` : existingRef;
  return {
    family: 'browser',
    ...(url ? { url } : {}),
    ...(title ? { title } : {}),
    ...(dataRef ? { screenshot: { mimeType, dataRef } } : {}),
  };
}

function computerActionFrom(
  value: unknown,
): Extract<ToolRichDetail, { family: 'computer' }>['action'] {
  switch (value) {
    case 'click':
    case 'type':
    case 'key':
    case 'scroll':
    case 'wait':
    case 'screenshot':
    case 'observe':
    case 'drag':
    case 'move':
      return value;
    default:
      return undefined;
  }
}

function computerResultStateFrom(
  value: unknown,
): Extract<ToolRichDetail, { family: 'computer' }>['resultState'] {
  switch (value) {
    case 'ok':
    case 'failed':
    case 'pending':
      return value;
    default:
      return undefined;
  }
}

function computerCoordinatesFrom(
  value: unknown,
): { readonly x: number; readonly y: number } | undefined {
  const record = pickRecord(value);
  const x = pickNumber(record?.x);
  const y = pickNumber(record?.y);
  return x == null || y == null ? undefined : { x, y };
}

function computerDetailFrom(
  value: unknown,
): Extract<ToolRichDetail, { family: 'computer' }> | null {
  const root = pickRecord(value);
  const result = pickRecord(root?.result);
  const partialResult = pickRecord(root?.partialResult);
  const container = pickRecord(root?.computer)
    ? root
    : pickRecord(result?.computer)
      ? result
      : pickRecord(partialResult?.computer)
        ? partialResult
        : null;
  const computer = pickRecord(container?.computer);
  if (!computer) return null;
  const blocks = contentBlocksFrom(container);
  const screenshot =
    screenshotFromImageValue(computer.screenshot) ??
    screenshotFromImageValue(container?.image) ??
    screenshotFromImageValue(blocks?.find(isImageBlock));
  const artifactPaths = Array.isArray(computer.artifactPaths)
    ? computer.artifactPaths.filter(
        (item): item is string => typeof item === 'string' && item.length > 0,
      )
    : undefined;
  return {
    family: 'computer',
    ...(computerActionFrom(computer.action) ? { action: computerActionFrom(computer.action) } : {}),
    ...(pickString(computer.targetApp, computer.target_app)
      ? { targetApp: pickString(computer.targetApp, computer.target_app) }
      : {}),
    ...(pickString(computer.targetWindow, computer.target_window)
      ? { targetWindow: pickString(computer.targetWindow, computer.target_window) }
      : {}),
    ...(pickString(computer.url) ? { url: pickString(computer.url) } : {}),
    ...(computerCoordinatesFrom(computer.coordinates)
      ? { coordinates: computerCoordinatesFrom(computer.coordinates) }
      : {}),
    ...(() => {
      const preview = cappedString(computer.textPreview ?? computer.text_preview, 160);
      return preview ? { textPreview: redactSensitiveText(preview) } : {};
    })(),
    ...(computerResultStateFrom(computer.resultState ?? computer.result_state)
      ? { resultState: computerResultStateFrom(computer.resultState ?? computer.result_state) }
      : {}),
    ...(screenshot ? { screenshot } : {}),
    ...(artifactPaths && artifactPaths.length > 0 ? { artifactPaths } : {}),
  };
}

/**
 * Parse a tool event's opaque `detail` JSON (the host emits `JSON.stringify` of
 * the tool args on `started` and the result on `completed`) into a
 * family-structured {@link ToolRichDetail}. Pure, deterministic, defensive: an
 * unparseable / key-absent detail degrades to the family with empty fields and
 * never throws. Key names are matched loosely (command/cmd, path/file_path/…,
 * pattern/query/…) because the exact tool arg/result shape varies by tool.
 */
export function parseToolRichDetail(toolName: string, detail?: string): ToolRichDetail {
  const family = toolFamily(classifyToolActivity(toolName));
  const detailValue = parseDetailValue(detail);
  const computer = computerDetailFrom(detailValue);
  if (computer) return computer;
  const browser = browserDetailFrom(detailValue);
  if (browser) return browser;
  const d = pickRecord(detailValue);
  const input = pickRecordFrom(d?.input);
  const argumentsValue = pickRecordFrom(d?.arguments ?? d?.args);
  const result = pickRecord(d?.result);
  const details = pickRecord(d?.details);
  if (family === 'terminal') {
    return {
      family,
      command: d
        ? pickString(
            d.command,
            d.cmd,
            input?.command,
            input?.cmd,
            argumentsValue?.command,
            argumentsValue?.cmd,
          )
        : undefined,
      exitCode: d
        ? pickNumber(
            d.exitCode,
            d.exit_code,
            d.code,
            input?.exitCode,
            input?.exit_code,
            result?.exitCode,
            result?.exit_code,
            result?.code,
            details?.exitCode,
            details?.exit_code,
            details?.code,
          )
        : undefined,
      outputSummary: d
        ? summarize(d.stdout ?? d.output ?? d.result ?? d.text ?? d.content)
        : undefined,
    };
  }
  if (family === 'file') {
    return {
      family,
      path: d
        ? pickString(
            d.path,
            d.file_path,
            d.filePath,
            d.file,
            input?.path,
            input?.file_path,
            input?.filePath,
            input?.file,
            argumentsValue?.path,
            argumentsValue?.file_path,
            argumentsValue?.filePath,
            argumentsValue?.file,
          )
        : undefined,
      summary: d ? summarize(d.diff ?? d.summary ?? d.result ?? d.text ?? d.content) : undefined,
    };
  }
  if (family === 'search') {
    const hitCount = d
      ? (pickNumber(d.count, d.hits) ??
        (Array.isArray(d.matches)
          ? d.matches.length
          : Array.isArray(d.results)
            ? d.results.length
            : undefined))
      : undefined;
    return {
      family,
      query: d
        ? pickString(
            d.pattern,
            d.query,
            d.regex,
            input?.pattern,
            input?.query,
            input?.regex,
            argumentsValue?.pattern,
            argumentsValue?.query,
            argumentsValue?.regex,
          )
        : undefined,
      hitCount,
    };
  }
  return { family: 'generic', text: summarize(detail) };
}

export function mergeToolRichDetail(
  previous: ToolRichDetail | undefined,
  next: ToolRichDetail,
): ToolRichDetail {
  if (!previous || previous.family !== next.family) return next;
  if (next.family === 'terminal' && previous.family === 'terminal') {
    return {
      family: 'terminal',
      command: next.command ?? previous.command,
      exitCode: next.exitCode ?? previous.exitCode,
      outputSummary: next.outputSummary ?? previous.outputSummary,
    };
  }
  if (next.family === 'file' && previous.family === 'file') {
    return {
      family: 'file',
      path: next.path ?? previous.path,
      summary: next.summary ?? previous.summary,
    };
  }
  if (next.family === 'search' && previous.family === 'search') {
    return {
      family: 'search',
      query: next.query ?? previous.query,
      hitCount: next.hitCount ?? previous.hitCount,
    };
  }
  if (next.family === 'browser' && previous.family === 'browser') {
    return {
      family: 'browser',
      url: next.url ?? previous.url,
      title: next.title ?? previous.title,
      screenshot: next.screenshot ?? previous.screenshot,
    };
  }
  if (next.family === 'computer' && previous.family === 'computer') {
    return {
      family: 'computer',
      action: next.action ?? previous.action,
      targetApp: next.targetApp ?? previous.targetApp,
      targetWindow: next.targetWindow ?? previous.targetWindow,
      url: next.url ?? previous.url,
      coordinates: next.coordinates ?? previous.coordinates,
      textPreview: next.textPreview ?? previous.textPreview,
      resultState: next.resultState ?? previous.resultState,
      screenshot: next.screenshot ?? previous.screenshot,
      artifactPaths: next.artifactPaths ?? previous.artifactPaths,
    };
  }
  if (next.family === 'generic' && previous.family === 'generic') {
    return { family: 'generic', text: next.text ?? previous.text };
  }
  return next;
}

/**
 * Delegation tool input. `executionMode: 'single'` runs exactly one child and
 * awaits it; `'parallel'` fans out one or more concurrently. `relation` is the
 * parent-child semantics of each task (defaulting to `review` only for
 * review-like tasks, else `delegate`) and is independent of the fan-out mode.
 */
export interface DelegateTaskInput {
  readonly employeeId: string;
  readonly objective: string;
  readonly access: AgentRunAccess;
  /** Optional work semantics for the delegated run (flows to `AgentRunEvent.workKind`). */
  readonly workKind?: WorkKind;
  /** Optional parent-child relation; resolved to a default when omitted. */
  readonly relation?: AgentRunRelation;
}

export interface DelegateToolInput {
  readonly tasks: readonly DelegateTaskInput[];
  readonly executionMode: DelegateExecutionMode;
}
