/**
 * Ordered (category, keyword-set) table for {@link classifyToolActivity}.
 * Whole-token matching avoids substring false hits (e.g. "budget" ⊅ get,
 * "rebuild" ⊅ build via substring). Earlier entries win on token collisions
 * (e.g. `run_tests` → test, not shell).
 */
const ACTIVITY_KEYWORDS: ReadonlyArray<readonly [ActivityKind, ReadonlySet<string>]> = [
  ['test', new Set(['test', 'tests', 'vitest', 'jest', 'pytest', 'qa'])],
  ['build', new Set(['build', 'compile', 'bundle', 'lint', 'typecheck'])],
  ['edit', new Set(['edit', 'patch', 'replace', 'apply', 'modify', 'refactor'])],
  ['write', new Set(['write', 'create', 'save', 'mkdir', 'touch', 'append'])],
  ['search', new Set(['search', 'grep', 'glob', 'find', 'list', 'ls', 'dir', 'locate'])],
  ['read', new Set(['read', 'cat', 'open', 'preview', 'view', 'fetch', 'get', 'show'])],
  ['shell', new Set(['bash', 'sh', 'shell', 'exec', 'run', 'command', 'cmd', 'terminal'])],
  ['wait', new Set(['wait', 'sleep', 'idle', 'poll'])],
];

/**
 * Classify a tool call into an {@link ActivityKind} from the tool *name* alone.
 *
 * Pure and deterministic: it tokenizes only the tool name (snake_case +
 * camelCase split), never arguments or shell content, so the model can't
 * influence the category and the renderer never has to parse raw tool I/O for
 * choreography. Unknown tools fall back to `inspect` ("looking at a result").
 */
export function classifyToolActivity(toolName: string): ActivityKind {
  const tokens = new Set(
    toolName
      .replace(/([a-z0-9])([A-Z])/g, '$1 $2')
      .toLowerCase()
      .split(/[^a-z0-9]+/)
      .filter(Boolean),
  );
  for (const [kind, keywords] of ACTIVITY_KEYWORDS) {
    for (const token of tokens) {
      if (keywords.has(token)) return kind;
    }
  }
  return 'inspect';
}

/** Coarse rendering family for a tool's rich detail view (D1). */
export type ToolFamily = 'terminal' | 'file' | 'search' | 'browser' | 'computer' | 'generic';

/** Map an {@link ActivityKind} to its rich-view {@link ToolFamily}. */
export function toolFamily(kind: ActivityKind): ToolFamily {
  switch (kind) {
    case 'shell':
    case 'build':
    case 'test':
      return 'terminal';
    case 'read':
    case 'write':
    case 'edit':
      return 'file';
    case 'search':
      return 'search';
    default:
      return 'generic';
  }
}

